#include "libft.h"

char	*ft_strdup(const char *s1)
{
	unsigned int		i;
	size_t				len;
	char				*str;

	len = ft_strlen(s1);
	if (!(str = (char*)malloc(sizeof(*str) * len + 1)))
		return (NULL);
	i = 0;
	while (i < len)
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
